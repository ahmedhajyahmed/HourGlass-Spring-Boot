package Pogo;

public class NutritionalPogo {

	public NutritionalPogo(String day, String description, int mealsNumber, String[] meals, String name) {
		super();
		this.day = day;
		this.description = description;
		this.mealsNumber = mealsNumber;
		this.meals = meals;
		this.name = name;
	}
	public String day;
	public String description;
	public int mealsNumber;
	public String[] meals;
	public String name;

}
